// Manuais e especificações

// Este livro é um tutorial. O objetivo é ajudá-lo a aprender gradualmente a língua. Mas uma vez que você está familiarizado com o básico, você vai precisar de outros recursos.
// Especificação de

// A especificação ECMA-262 contém as informações mais detalhadas, detalhadas e formalizadas sobre JavaScript. Isso define a linguagem.

// Mas sendo isso formalizado, é difícil de entender no início. Então, se você precisar da fonte mais confiável de informações sobre os detalhes do idioma, a especificação é o lugar certo. Mas não é para uso diário.

// Uma nova versão de especificação é lançada todos os anos. Entre essas versões, o rascunho de especificação mais recente está em https://tc39.es/ecma262/.

// Para ler sobre novos recursos de ponta, incluindo aqueles que são “quase padrão” (o chamado “estágio 3”), consulte as propostas em https://github.com/tc39/propostas.

// Além disso, se você está desenvolvendo para o navegador, então existem outras especificações cobertas na segunda parte do tutorial.
// Manuais de construção

//     MDN (Mozilla) JavaScript Reference é o principal manual com exemplos e outras informações. É ótimo obter informações detalhadas sobre funções individuais de linguagem, métodos, etc.

//     Você pode encontrá-lo em https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference.

// Embora, muitas vezes, é melhor usar uma pesquisa na internet em vez disso. Basta usar “MDN [termo]” na consulta, por exemplo, https://google.com/search? q'MDN+parseInt para procurar o parseIntFunção.
